// Helper functions
export const getProductById = (id: number): ProductData | undefined =>
  allProducts.find(p => p.id === id);

export const getProductsByCategory = (category: ProductData['category']): ProductData[] =>
  allProducts.filter(p => p.category === category);

export const getProductsBySubcategory = (subcategory: string): ProductData[] =>
  allProducts.filter(p => p.subcategory === subcategory);

export const getProductsBySku = (sku: string): ProductData | undefined =>
  allProducts.find(p => p.sku === sku);

// Get random products from a category
export const getRandomProducts = (category: ProductData['category'], count: number): ProductData[] => {
  const categoryProducts = getProductsByCategory(category);
  const shuffled = [...categoryProducts].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, categoryProducts.length));
};

// Get products suitable for a vendor sector
export const getProductsForSector = (sector: string): ProductData[] => {
  const sectorMapping: Record<string, ProductData['category'][]> = {
    'IT/Electronics': ['IT/Electronics'],
    'Software': ['IT/Electronics'],
    'Office Supplies': ['Office Supplies'],
    'Office Furniture': ['Office Supplies'],
    'Manufacturing': ['Manufacturing'],
    'Raw Materials': ['Manufacturing'],
    'Safety/PPE': ['Manufacturing'],
    'Packaging': ['Manufacturing'],
    'Logistics': ['Manufacturing', 'Office Supplies'],
    'Testing/QA': ['Manufacturing'],
    'Sustainable': ['Office Supplies', 'Manufacturing'],
  };

  const categories = sectorMapping[sector] || ['IT/Electronics', 'Office Supplies', 'Manufacturing'];
  return allProducts.filter(p => categories.includes(p.category));
};
