// Helper to get company by ID
export const getCompanyById = (id: number): CompanyData | undefined =>
  allCompanies.find(c => c.id === id);

// Helper to get vendor rating
export const getVendorRating = (companyId: number): number => {
  const company = getCompanyById(companyId);
  return company?.overallRating || 3.0;
};
